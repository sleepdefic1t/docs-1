# ARK: Registering an IPFS Hash

This transaction type lets you register an [Interplanetary File System (IPFS) Hash](https://ipfs.io/#how) on the ARK Public Network (APN).

This is useful for timestamping and sharing the file hash of important documents or images.

<x-alert type="warning">
Storing an IPFS hash is **not** the same as storing IPFS data. That requires an [IPFS Node](https://docs.ipfs.io/how-to/companion-node-types/).
</x-alert>

## Getting Started

To get started, click the 3-button menu of the wallet from which you'd like to register an IPFS hash, then select **'Store Hash'** from the drop-down menu.

<div style="width: 300px; float: inherit; margin: auto">

![](/storage/docs/docs/wallet/assets/transactions/transactions-ark-ipfs-menu-focus.png)

</div>

## Entering the IPFS Hash

Next, enter the hash you wish to register on-chain into the **'IPFS Hash'** field shown below.

---

When you're ready to proceed, click the **'Continue**' button.

<div style="width: 500px; float: inherit; margin: auto">

![](/storage/docs/docs/wallet/assets/transactions/transactions-ark-ipfs-details.png)

</div>

<x-alert type="success">
Learn more about ARK transaction fees by visiting the ['ARK Fees'](/docs/wallet/transactions/ark/fees) documentation page.
</x-alert>

## Reviewing the Transaction

You'll now be allowed to review the transaction's details

After verifying that everything is correct, click the **'Continue**' button.

<div style="width: 500px; float: inherit; margin: auto">

![](/storage/docs/docs/wallet/assets/transactions/transactions-ark-ipfs-review.png)

</div>

## Authenticating the Transaction

Finally, before sending, you'll be asked to sign the transaction using your wallet's credentials (e.g. mnemonic recovery phrase, secret phrase, encryption password, etc).

Enter your signing credentials in the provided field, then click the **'Send'** button when you're ready to broadcast your transaction.

<div style="width: 500px; float: inherit; margin: auto">

![](/storage/docs/docs/wallet/assets/transactions/transactions-ark-ipfs-authenticate.png)

</div>

## Success

Congrats! You've successfully stored an IPFS Hash on the ARK Public Network!


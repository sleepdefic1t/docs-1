# ARK: Staking Commissions

The ARK Public Network (**APN**) is a sovereign Blockchain with a native Cryptoasset called ARK. It is based on the Delegated-Proof-of-Stake (DPoS) consensus mechanism and has 51 active Validators (formerly known as ARK Delegates) who secure the network. In this article, we will discuss how block commissions work on the APN and how you can participate and potentially earn a portion of the commissions by becoming an active Validator or voting with your ARK coins.

## Block Commissions

The APN is a sovereign blockchain that is validated by 51 active Validators.

**key facts**:

* Each Validator will submit (1) block per round.
* A round consists of 51 blocks.
* Each block on the APN has a block time of 8 seconds.
* Each block awards the active Validator who submits the block a block commission of 2 ARK along with any transaction fees from that block.
* Block Commissions are inflationary coins, meaning the total ARK supply is increased by 2 ARK every block. This serves as a form of consistent inflation that reduces over time due to the non-fluctuating rate of commissions.
* There are no "halvings" of block commissions on the APN.
* On the APN, 100% of the block commission goes to the Validator.

The block commissions and transaction fees serve as a form of payment for the service provided to the network on behalf of the voters who have delegated their influence to the Validator and entrusted them with the security and reliability of the network.

## Voting on the ARK Public Network

The APN uses **voting** as a form of **staking**. To vote for a Validator and delegate your influence to them, you do not need to *lock* your coins. This allows ARK holders to have more freedom and access to their coins while still allowing them to participate in the governance of the network.


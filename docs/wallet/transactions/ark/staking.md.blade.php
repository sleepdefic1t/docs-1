## Commissions Sharing on the ARK Public Network

As previously stated, **block commissions** and **transaction fees** are awarded to the active Validator responsible for reporting a given block. We call this combination of the 2 ARK block commission and any fees the **total commission**. On the APN, many of these Validators choose to take a small commission from the **total rewards** and share the remaining **after commission reward (ACR)** with their voters.

<x-alert type="info">
Different Validators offer different proposals in exchange for your vote. Some are public Validators, some are Private. Some share commissions or services, Some do not.

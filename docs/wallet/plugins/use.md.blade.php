# Using Plugins

On this page, you will learn how to enable, disable, and launch a plugin.

In this particular instance, we will be demonstrating the process using the ARK Block Explorer plugin. The exact steps may vary from plugin to plugin, but the process should stay relatively the same.

## Getting Started

Get started by navigating to the 'My Plugins' page of the 'Plugin Manager'.

![](/storage/docs/docs/wallet/assets/plugins/plugins-use-my-plugins.png)

## Enabling the Plugin

Before using a plugin, you must first 'enable' it.

From the 'My Plugins' page, click the 3-dot menu of your chosen plugin and select **'Enable'**.

![](/storage/docs/docs/wallet/assets/plugins/plugins-use-enable-menu.png)

---

If your plugin was successfully enabled, you'll see a notification in the lower-right corner of the Payvo Wallet.

<div style="width: 500px; float: inherit; margin: auto">

![](/storage/docs/docs/wallet/assets/plugins/plugins-use-enabled.png)

</div>

## Disabling a Plugin

If you'd ever like to prevent the launching of a particular plugin but don't want it fully uninstalled, you may also choose to disable it.

From your profile's 'My Plugins' page, click the 3-dot menu of your chosen plugin and select **'Disable'**.

![](/storage/docs/docs/wallet/assets/plugins/plugins-use-disable-menu.png)

## Launching a Plugin

Now that your plugin has successfully been enabled, you may proceed to launch it.

From the 'My Plugins' page, click on your chosen plugin. You'll then select the **'Launch'** button towards the top-right of its page.

![](/storage/docs/docs/wallet/assets/plugins/plugins-use-launch.png)

---

You may also launch a plugin from the 'My Plugins' page by clicking the 3-dot menu of your chosen plugin and selecting **'Launch'**.

![](/storage/docs/docs/wallet/assets/plugins/plugins-use-launch-menu.png)

## Success

Congrats! You've successfully launched the ARK Block Explorer Plugin!

![](/storage/docs/docs/wallet/assets/plugins/plugins-use-launched.png)

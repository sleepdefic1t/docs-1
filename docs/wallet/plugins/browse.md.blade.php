# Browsing Plugins

Use the **'Plugins Manager'** to find games, utilities, and more to enhance your Payvo Wallet experience. See the newest additions, sort by various categories, or even search for your favorites by name.

<div style="width: 800px; float: inherit; margin: auto">

![](/storage/docs/docs/wallet/assets/plugins/plugins-manager.png)

</div>

## Getting Started

Access the **'Plugins Manager'** by clicking **'Plugins'** at the top of the Payvo Wallet while signed-in to your profile.

<div style="width: 800px; float: inherit; margin: auto">

![](/storage/docs/docs/wallet/assets/plugins/plugins-browse-focus.png)

</div>

## Sorting by Category

Looking for a specific kind of plugin or just want to see the latest and greatest? Use the navigation tabs seen below:

![](/storage/docs/docs/wallet/assets/plugins/plugins-browse-categories.png)

* Latest
* All
* Gaming
* Utility
* Other


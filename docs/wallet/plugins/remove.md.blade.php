# Removing a Plugin

Removing a plugin is simple and can be done in a few easy steps. This fully deletes the plugin for all profiles.

<x-alert type="info">
Plugins are shared resources. Installing or removing a plugin installs or removes that particular plugin for all Payvo Wallet profiles.
</x-alert>

## Getting Started

To get started, locate the plugin you'd like to remove, and select **'Delete'** from its 3-dot dropdown menu.

<div style="width: 500px; float: inherit; margin: auto">

![](/storage/docs/docs/wallet/assets/plugins/plugins-remove-menu.png)

</div>

<x-alert type="info">
You may also 'Disable' this plugin if you do not wish to have it permanently removed.
</x-alert>

## Confirming the Removal

If you'd like to continue with removing this plugin, click the **'Delete'** button to confirm your decision or **'Cancel'** to exit the removal process.

<div style="width: 400px; float: inherit; margin: auto">

![](/storage/docs/docs/wallet/assets/plugins/plugins-remove-confirm.png)

</div>

<x-alert type="danger">
Remember that access to this Plugin will be removed for all profiles.
</x-alert>

## Success

Congrats! You've successfully deleted a plugin!

<div style="width: 500px; float: inherit; margin: auto">

![](/storage/docs/docs/wallet/assets/plugins/plugins-remove-success.png)

</div>

# What are Plugins?

Plugins are installable extensions that let you play games, view block explorers, use custom avatars or notification sounds, and more, all from inside the Payvo Wallet.

Plugins are also open-source and auditable! This not only means better security for users, it means developers can implement and distribute custom extensions that bring their latest and greatest ideas to life.


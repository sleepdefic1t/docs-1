## Integration Requests

We are currently not taking community integration requests. If you would like a specific cryptoasset to be added to the Payvo Wallet, please encourage them to register at [MarketSquare](https://marketsquare.io) and reach out to us directly.

We will add new assets based on a series of factors to include:

* Ease of Integration
* Community Size
* Ecosystem Fit
* MarketSquare Participation

<x-alert type="warning">
The ARK.io Team does not charge a listing fee and will not ask for Coins, Tokens, or other payment for listing. If someone is asking you for a deposit, you are being scammed.
</x-alert>


## What is Payvo?

[Payvo](https://payvo.com/) is a new brand focused on empowering everyday people to take control of their finances through tools that allow them to manage, spend, receive, account for, and track all of their crypto-based activities. From the moment you receive your first tokens to the day you file your taxes, whether you are a shop owner or a regular user, Payvo has you covered.

## What is the Payvo Wallet?

The Payvo Wallet expands on the success of the ARK Desktop Wallet, focuses on prioritizing daily interaction with your cryptoassets, and is available for Linux, macOS, and Windows. It provides an extensive set of functionalities, including management of profiles and wallets, the creation of cryptoasset transactions, wallet summary, stylistic customizations, multilingual support, and various currency integrations!


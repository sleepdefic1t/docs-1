# Deleting a Wallet

If you no longer wish to use a particular wallet, it may be deleted at any time. The transactions and all on-chain data will not be affected as deleting a wallet only removes access to it from the Payvo Wallet.

Should you ever need to restore access to a deleted wallet, it may be imported using its associated mnemonic recovery phrase.

## Getting Started

While signed in to your profile, click the wallet you'd like to delete.

![](/storage/docs/docs/wallet/assets/wallets/wallets-delete.png)

## Selecting the Delete Option

Once you've entered the wallet you wish to delete, click the 3-button menu, then select **'Delete'** from the dropdown menu.

![](/storage/docs/docs/wallet/assets/wallets/wallets-delete-menu-focus.png)

## Confirming the Deletion

<x-alert type="danger">
This action **cannot** be reversed. Make sure your mnemonic recovery passphrase is backed up securely before proceeding.
</x-alert>

You'll then be asked to confirm your decision. Click **'Cancel'** to abort the process, or click the **'Delete'** button to continue removing your chosen wallet.

![](/storage/docs/docs/wallet/assets/wallets/wallets-delete-confirm.png)

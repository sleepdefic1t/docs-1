# Importing Ledger Wallets

The Payvo Wallet lets you import wallets and sign transactions or messages using the Ledger NanoX & NanoS. These devices improve the security of managing assets by performing all key and signing operations on the device itself. This keeps your private credentials isolated from any internet-connected device you might interact with.

## Supported Networks

Each supported network requires that your Ledger device first installs the associated Ledger device App.

Below is a list of Ledger-supported networks. Click on your desired network to learn more about supported operations as well as how to install its associated Ledger App:

* **[ARK](/docs/wallet/transactions/ark/ledger)**

## Importing a Wallet

Conceptually speaking, creating a Ledger wallet is more or less the same as importing a Ledger wallet. For simplicity, we refer to either as "importing a ledger wallet".

### Getting Started

From the profile for which you'd like to import a Ledger wallet, click the **'Import Ledger'** button.

![](/storage/docs/docs/wallet/assets/wallets/wallets-import-ledger-focus.png)

### Connecting your Ledger Device

<x-alert type="warning">
Any other software that might connect to your Ledger should remain closed while using the Payvo Wallet as it may interfere with the connection. For example, Ledger Live.
</x-alert>

Next, plug your Ledger device into your computer using a USB-data cable, preferably the cable included with your Ledger NanoX or NanoS.

<x-alert type="info">
You'll also need to unlock your Ledger device using the pin you created while setting up your device.
</x-alert>

### Selecting your Network

You'll now be given the option of selecting the cryptoasset network for which you'd like to import a Ledger wallet.

Once you've made your selection, click the **'Continue'** button.

<div style="width: 500px; float: inherit; margin: auto">

![](/storage/docs/docs/wallet/assets/wallets/wallets-import-ledger-network.png)

</div>

### Opening the Network's Ledger App

Next, you'll be asked to open the network's associated Ledger app on your Ledger device.

<div style="width: 500px; float: inherit; margin: auto">

![](/storage/docs/docs/wallet/assets/wallets/wallets-import-ledger-waiting-app.png)

</div>

---

Your Ledger device will then begin searching for available wallet addresses for you to import.

<div style="width: 500px; float: inherit; margin: auto">

![](/storage/docs/docs/wallet/assets/wallets/wallets-import-ledger-waiting.png)

</div>

### Selecting the Accounts

After some time, you'll be presented with the option to select which wallets you'd like to import.

For new/unused Ledger devices, you will typically see at least one wallet available for import. If you have used this Ledger device before, it is common to see many wallets available for import.

---

After selecting the wallet address(es) you'd like to import, click the **'Continue'** button.

<div style="width: 500px; float: inherit; margin: auto">

![](/storage/docs/docs/wallet/assets/wallets/wallets-import-ledger-accounts.png)

</div>

<x-alert type="info">
The number of available wallets can vary by network.
</x-alert>

### Success

Congrats! You've successfully imported your chosen network's Ledger wallet address(es)!

<div style="width: 500px; float: inherit; margin: auto">

![](/storage/docs/docs/wallet/assets/wallets/wallets-import-ledger-completed.png)

</div>

<x-alert type="success">
These imported Ledger wallets will be visible from your profile's main page.
</x-alert>

# Managing Contacts

"My Contacts" are a personalized offline directory where you can store and organize the network addresses of people and/or services you interact frequently with. These are unique, per-profile, and are included in a profile's backup.

![](/storage/docs/docs/wallet/assets/profiles/profiles-contacts.png)

---

On this page, you can learn more about the following:

* [Adding a Contact](#adding-a-contact)
* [Deleting a Contact](#deleting-a-contact)
* [Using a Contact](#using-a-contact)

## Adding a Contact

Adding a contact can be done in just a few steps. You can also create contacts with multiple addresses across different networks.

### Getting Started

Get started by clicking your profile avatar and selecting **'Contacts'** from the dropdown menu.

<div style="width: 250px; float: inherit; margin: auto">

![](/storage/docs/docs/wallet/assets/profiles/profiles-contacts-menu.png)

</div>

---

This will take you to your profile's 'My Contacts' page.

Click the **'Add Contact'** button to proceed with creating a new contact.

![](/storage/docs/docs/wallet/assets/profiles/profiles-contacts-add-focus.png)

### Entering the Contact Details

Next, you'll be asked to enter the details for your new contact.

<div style="width: 500px; float: inherit; margin: auto">

![](/storage/docs/docs/wallet/assets/profiles/profiles-contacts-add-modal.png)

</div>

* **Name**:
  * A unique name you'd like to use for this specific contact.
  * **Must** be 1 to 42 characters in length.
  * This name will be shown on your profile's 'My Contacts' page.
  * This name will also be provided as a recipient option when sending transactions on networks for which this contact has an associated address.
* **Cryptoasset**:
  * The Network Asset that corresponds to the address you are adding to a given contact.
* **Address**:
  * The network-associated address for your new contact.
  * **Must** correspond to the selected cryptoasset.

---

Add as many cryptoasset addresses for this contact as you'd like!

<div style="width: 500px; float: inherit; margin: auto">

![](/storage/docs/docs/wallet/assets/profiles/profiles-contacts-add-2.png)

</div>

<x-alert type="info">
You may add 1 address per cryptoasset for a single contact.
</x-alert>

---

When you're finished entering your contact's detail, click the **'Save'** button.

<div style="width: 500px; float: inherit; margin: auto">

![](/storage/docs/docs/wallet/assets/profiles/profiles-contacts-add-3.png)

</div>

### Success

Congrats! You have successfully created a new contact!

![](/storage/docs/docs/wallet/assets/profiles/profiles-contacts-added.png)

## Deleting a Contact

To permanently delete a contact, navigate to your contacts page.

Next, click the 3-dot menu button of the contact you'd like to remove, then select **'Delete'** from the dropdown menu.

![](/storage/docs/docs/wallet/assets/profiles/profiles-contacts-delete-menu.png)

---

You'll then be asked to confirm your decision. Click **'Delete'** to continue deleting the contact.

<div style="width: 400px; float: inherit; margin: auto">

![](/storage/docs/docs/wallet/assets/profiles/profiles-contacts-delete-confirm.png)

</div>

## Using a Contact

To initiate a transaction from your profile's 'My Contact' page, click the **'Send'** button next to the contact address you'd like to send funds to.

![](/storage/docs/docs/wallet/assets/profiles/profiles-contacts-send-tx-1.png)

---

While creating a transaction, you can also select your contact as a recipient. If your contact is not showing up, make sure they have an address for the cryptoasset network you're attempting to use.

<div style="width: 400px; float: inherit; margin: auto">

![](/storage/docs/docs/wallet/assets/profiles/profiles-contacts-send-tx-2.png)

</div>

# What are Profiles?

Profiles are an excellent new way to manage your cryptoassets. They are essentially unique accounts that allow you to transfer & receive funds, vote for Validators, manage contacts, and import or create wallets for multiple networks all in one place.

Get the most out of your Payvo Wallet experience with customizable display options and language settings. You can even create multiple profiles, each with a unique purpose.

<x-alert type="success">
With Payvo Profiles, _you_ are in the driver's seat!
</x-alert>


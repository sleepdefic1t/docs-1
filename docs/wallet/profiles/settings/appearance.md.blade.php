# Profile Settings: Appearance

'Appearance' settings allow you to customize your Payvo Wallet experience by choosing color schemes and other miscellaneous display options.

![](/storage/docs/docs/wallet/assets/profiles/profiles-settings-appearance.png)

## Accent Color

The 'Accent Color' is used for some of the buttons and highlighting throughout your profile. 'Green' is enabled by default.

![](/storage/docs/docs/wallet/assets/profiles/profiles-settings-appearance-color.png)

| Blue Accent | Green Accent |
| :---------: | :----------: |
| ![](/storage/docs/docs/wallet/assets/profiles/profiles-settings-appearance-color-blue.png) | ![](/storage/docs/docs/wallet/assets/profiles/profiles-settings-appearance-color-green.png) |

## Viewing Mode

'Viewing Mode' is where you can choose a light or dark mode for your Payvo Wallet profile.

Your OS's theme is used by default. For example, if you have macOS and are using Dark Mode, the Payvo Wallet will default to using the Dark mode as well.

![](/storage/docs/docs/wallet/assets/profiles/profiles-settings-appearance-mode.png)

| Light Mode | Dark Mode |
| :--------: | :-------: |
| ![](/storage/docs/docs/wallet/assets/profiles/profiles-settings-appearance-light.png) | ![](/storage/docs/docs/wallet/assets/profiles/profiles-settings-appearance-dark.png) |

## Portfolio Transaction History

This setting determines whether or not to display your profile's transaction history on your Portfolio page. This includes transactions from all wallets associated with your profile.

Displaying your transaction history is enabled by default and may be disabled if you wish to hide these details for any reason.

![](/storage/docs/docs/wallet/assets/profiles/profiles-settings-appearance-history.png)

## Wallet Naming

This setting determines whether or not to use network-provided names.

By default (disabled), your profile's wallet and contact names will be shown instead of network-provided names. When enabled, network-provided names will be shown instead of your profile's wallet and contact names.

![](/storage/docs/docs/wallet/assets/profiles/profiles-settings-appearance-naming.png)

## Expanded Tables

This setting determines whether to increase the size of tables shown in your profile. Compact tables are used by default.

![](/storage/docs/docs/wallet/assets/profiles/profiles-settings-appearance-tables.png)

| Compact Tables | Expanded Tables |
| :------------: | :-------------: |
| ![](/storage/docs/docs/wallet/assets/profiles/profiles-settings-appearance-tables-compact.png) | ![](/storage/docs/docs/wallet/assets/profiles/profiles-settings-appearance-tables-expanded.png) |

## Applying Changes

When you're finished setting your appearance options, click the **'Save'** button to confirm all changes.

![](/storage/docs/docs/wallet/assets/profiles/profiles-settings-appearance-save.png)

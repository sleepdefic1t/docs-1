# Deleting a Profile

Deleting a profile from the Payvo Wallet can be done from the profile sign-in page. This permanently erases your profile and all associated information, including settings, wallets, and contacts.

<x-alert type="danger">
This action **cannot** be reversed. Make sure that any wallet recovery phrases or contact addresses you wish to keep are backed up before proceeding.
</x-alert>

## Getting Started

From the sign-in page, click the 3-dot menu of the profile you wish to delete.

<div style="width: 500px; float: inherit; margin: auto">

![](/storage/docs/docs/wallet/assets/profiles/profiles-delete-focus.png)

</div>

## Clicking 'Delete'

Click the **'Delete'** menu option to continue.

<div style="width: 500px; float: inherit; margin: auto">

![](/storage/docs/docs/wallet/assets/profiles/profiles-delete-menu.png)

</div>

## Confirming Deletion

<x-alert type="danger">
**Do not** proceed unless you have checked and double-checked that any information you wish to keep is backed up securely. This action **cannot** be reversed.
</x-alert>

Click the **'Delete'** button to confirm your decision.

<div style="width: 400px; float: inherit; margin: auto">

![](/storage/docs/docs/wallet/assets/profiles/profiles-delete-confirm.png)

</div>

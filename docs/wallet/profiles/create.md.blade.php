# Creating a Profile

Creating a profile in the Payvo Wallet is simple. In just a few steps, you'll be set up and managing your assets across multiple wallets and currencies of your choosing. You can even create multiple profiles to help further organize your assets.

![](/storage/docs/docs/wallet/assets/profiles/profiles-welcome.png)

## Getting Started

To get started, click the **'Create'** button on the Payvo Wallet Sign-in page.

![](/storage/docs/docs/wallet/assets/profiles/profiles-create-focus.png)

## Profile Info

Next, you'll be allowed to enter information for your new profile.

![](/storage/docs/docs/wallet/assets/profiles/profiles-create-info.png)

* **Profile Name**:
  * Must be at least 1 to 42 characters in length.
  * Displayed on the sign-in page.
  * Used to auto-generate your default profile avatar.
* **Profile Avatar** (_*optional_):
  * The primary image associated with your profile.
  * Auto-generated from your username by default.
  * Supports custom images.
  * Displayed on the sign-in page and at the top-right of Payvo Wallet after signing in.
* **Password** (_*optional_):
  * Used for locking your profile so others cannot access it.
  * Also used when [importing your profile](/docs/wallet/profiles/import).
  * **_Not_** associated with wallet recovery phrases.
  * **Requirements**:
    * 8 characters minimum
    * 1 upper-case
    * 1 lower-case
    * 1 number
    * 1 special character
* **Currency**:
  * The preferred currency for balances and operations that are shown throughout your profile.
  * eg _USD, CAD, EUR, BTC, etc._
* **Dark Theme** (default: OS theme):
  * a high-contrast theme that uses your OS's theme settings by default.

<x-alert type="warning">
If you forget your profile's password, there is ***no*** way to recover it. You will need to create a new profile and [re-import your wallets](/docs/wallet/wallets/import).
</x-alert>

---

When you're finished, click the **'Create'** button.

![](/storage/docs/docs/wallet/assets/profiles/profiles-create-ready.png)

